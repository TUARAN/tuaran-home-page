/** 默认风格（status === 'active'） */
export function getActiveTemplate() {
  return RESEARCH_STYLE_TEMPLATES.find((t) => t.status === 'active') || RESEARCH_STYLE_TEMPLATES[0]
}

/** 按 id 查找风格 */
export function getTemplateById(id) {
  return RESEARCH_STYLE_TEMPLATES.find((t) => t.id === id)
}

/** 保留旧函数名：按配置顺序返回，不再表达版本时间线。 */
