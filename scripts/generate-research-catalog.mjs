import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const categories = ['companies', 'topics', 'people']
const filenameDateRe = /^(\d{4}-\d{2}-\d{2})-(.+)\.md$/i

const keys = []
const articleRedirects = {}
for (const category of categories) {
  const dir = path.join(root, 'research', category)
  let files = []
  try {
    files = fs.readdirSync(dir)
  } catch {
    continue
  }

  for (const file of files) {
    const match = filenameDateRe.exec(file)
    if (!match) continue
    const slug = match[2]
    const legacySlug = file.replace(/\.md$/i, '')
    const key = `${category}/${slug}`
    const canonicalPath = `/articles/research/${category}/${slug}`
    keys.push(key)
    articleRedirects[slug] = canonicalPath
    articleRedirects[legacySlug] = canonicalPath
  }
}

keys.sort()
const redirectEntries = Object.entries(articleRedirects).sort(([a], [b]) => a.localeCompare(b))

const content = `// Generated by scripts/generate-research-catalog.mjs. Do not edit by hand.
export const RESEARCH_ENTRY_KEYS = [
${keys.map((key) => `  '${key}',`).join('\n')}
]

export const RESEARCH_ENTRY_KEY_SET = new Set(RESEARCH_ENTRY_KEYS)

export const RESEARCH_ARTICLE_REDIRECTS = {
${redirectEntries.map(([slug, href]) => `  '${slug}': '${href}',`).join('\n')}
}
`

fs.writeFileSync(path.join(root, 'lib', 'research', 'catalog.js'), content)
