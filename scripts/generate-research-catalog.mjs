import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const categories = ['companies', 'topics', 'people']
const filenameDateRe = /^(\d{4}-\d{2}-\d{2})-(.+)\.md$/i

const keys = []
const articleRedirects = {}
const entryMeta = {}

function parseFrontmatter(raw) {
  if (!raw.startsWith('---')) return {}
  const end = raw.indexOf('\n---', 3)
  if (end === -1) return {}
  const yaml = raw.slice(3, end).trim()
  const data = {}
  for (const line of yaml.split(/\r?\n/)) {
    const match = /^([A-Za-z_][A-Za-z0-9_]*)\s*:\s*(.*)$/.exec(line)
    if (!match) continue
    let value = match[2].trim()
    if (!value) continue
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1)
    }
    data[match[1]] = value
  }
  return data
}

for (const category of categories) {
  const dir = path.join(root, 'research', category)
  let files = []
  try {
    files = fs.readdirSync(dir)
  } catch {
    continue
  }

  for (const file of files) {
    const match = filenameDateRe.exec(file)
    if (!match) continue
    const slug = match[2]
    const legacySlug = file.replace(/\.md$/i, '')
    const key = `${category}/${slug}`
    const canonicalPath = `/articles/research/${category}/${slug}`
    keys.push(key)
    articleRedirects[slug] = canonicalPath
    articleRedirects[legacySlug] = canonicalPath

    let data = {}
    try {
      data = parseFrontmatter(fs.readFileSync(path.join(dir, file), 'utf8'))
    } catch {
      data = {}
    }
    const date = data.date || match[1]
    const time = data.time || ''
    entryMeta[key] = {
      category,
      slug,
      date,
      ...(time ? { time } : {}),
      title: data.title || slug,
      summary: data.tldr || data.summary || '',
    }
  }
}

keys.sort()
const redirectEntries = Object.entries(articleRedirects).sort(([a], [b]) => a.localeCompare(b))
const metaEntries = Object.entries(entryMeta).sort(([a], [b]) => a.localeCompare(b))

const content = `// Generated by scripts/generate-research-catalog.mjs. Do not edit by hand.
export const RESEARCH_ENTRY_KEYS = [
${keys.map((key) => `  '${key}',`).join('\n')}
]

export const RESEARCH_ENTRY_KEY_SET = new Set(RESEARCH_ENTRY_KEYS)

export const RESEARCH_ARTICLE_REDIRECTS = {
${redirectEntries.map(([slug, href]) => `  '${slug}': '${href}',`).join('\n')}
}

export const RESEARCH_ENTRY_META = {
${metaEntries.map(([key, meta]) => `  '${key}': ${JSON.stringify(meta)},`).join('\n')}
}
`

fs.writeFileSync(path.join(root, 'lib', 'research', 'catalog.js'), content)
